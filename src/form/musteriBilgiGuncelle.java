    private void btn_guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guncelleActionPerformed

        if (txt_yas.getText().equalsIgnoreCase("") || txt_boy.getText().equalsIgnoreCase("") || txt_kilo.getText().equalsIgnoreCase("") || txt_yag.getText().equalsIgnoreCase("") || (!radiobutton_erkek.isSelected() && !radiobutton_kadın.isSelected()) || (!radiobutton_kiloalmak.isSelected() && !radiobutton_yagyakmak.isSelected())) {

            JOptionPane.showMessageDialog(this, " (*) ile işaretli alanlar boş bırakılamaz!");

        } else if (radiobutton_erkek.isSelected() && radiobutton_kadın.isSelected()) {
            JOptionPane.showMessageDialog(this, " İki cinsiyet seçimi yapılamaz!");
        } else if (radiobutton_kiloalmak.isSelected() && radiobutton_yagyakmak.isSelected()) {
            JOptionPane.showMessageDialog(this, " İki hedef aynı anda seçilemez!");
        } else {
            this.getSystemFacede().getMusteriSystem().getM().setAd_soyad(txt_adSoyad.getText());
            this.getSystemFacede().getMusteriSystem().getM().setLogin_name(txt_loginName.getText());
            this.getSystemFacede().getMusteriSystem().getM().setPassword(txt_password.getText());
            this.getSystemFacede().getMusteriSystem().getM().setTelefon(txt_telefon.getText());
            this.getSystemFacede().getMusteriSystem().getM().setEmail(txt_email.getText());
            this.getSystemFacede().getMusteriSystem().getM().setYas(Integer.parseInt(txt_yas.getText()));
            this.getSystemFacede().getMusteriSystem().getM().setBoy(Integer.parseInt(txt_boy.getText()));
            this.getSystemFacede().getMusteriSystem().getM().setKilo(Double.parseDouble(txt_kilo.getText()));
            this.getSystemFacede().getMusteriSystem().getM().setYag_orani(Double.parseDouble(txt_yag.getText()));
            if (radiobutton_erkek.isSelected()) {
                this.getSystemFacede().getMusteriSystem().getM().setCinsiyet(radiobutton_erkek.getText());
            } else {
                this.getSystemFacede().getMusteriSystem().getM().setCinsiyet(radiobutton_kadın.getText());
            }
            if (radiobutton_kiloalmak.isSelected()) {
                this.getSystemFacede().getMusteriSystem().getM().setHedef(radiobutton_kiloalmak.getText());
            } else {
                this.getSystemFacede().getMusteriSystem().getM().setHedef(radiobutton_yagyakmak.getText());
            }
            if (checkBox_bildirim.isSelected()) {
                this.getSystemFacede().getMusteriSystem().getM().setBildirim(true);
            } else {
                this.getSystemFacede().getMusteriSystem().getM().setBildirim(false);
            }
            this.getSystemFacede().getMusteriSystem().update();

            this.mform.musteri_goster(this.systemFacede.getMusteriSystem().getM());
            this.mform.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btn_guncelleActionPerformed

