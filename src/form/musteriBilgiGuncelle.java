    public void bilgileriGetir(Musteri m) {
        txt_adSoyad.setText(m.getAd_soyad());
        txt_loginName.setText(m.getLogin_name());
        txt_password.setText(m.getPassword());
        txt_email.setText(m.getEmail());
        txt_telefon.setText(m.getTelefon());
        txt_yas.setText(String.valueOf(m.getYas()));
        txt_boy.setText(String.valueOf(m.getBoy()));
        txt_kilo.setText(String.valueOf(m.getKilo()));
        txt_yag.setText(String.valueOf(m.getYag_orani()));
        if (m.getCinsiyet().equalsIgnoreCase(radiobutton_erkek.getText())) {
            radiobutton_erkek.doClick();
        } else {
            radiobutton_kadın.doClick();
        }
        if (m.getHedef().equalsIgnoreCase(radiobutton_kiloalmak.getText())) {
            radiobutton_kiloalmak.doClick();
        } else {
            radiobutton_yagyakmak.doClick();
        }
        if (m.isBildirim()) {
            checkBox_bildirim.doClick();
        }
    }

