    private void btn_guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guncelleActionPerformed
        this.model = (DefaultTableModel) table_besin.getModel();
        
        this.getSystemFacede().getBesinSystem().getBesin().setBesin_id(Integer.parseInt(model.getValueAt(table_besin.getSelectedRow(), 0).toString()));
        this.getSystemFacede().getBesinSystem().getBesin().setIsim(txt_isim.getText());
        this.getSystemFacede().getBesinSystem().getBesin().setKalori(Double.parseDouble(txt_kalori.getText()));
        this.getSystemFacede().getBesinSystem().getBesin().setKarbonhidrat(Double.parseDouble(txt_karbonhidrat.getText()));
        this.getSystemFacede().getBesinSystem().getBesin().setProtein(Double.parseDouble(txt_protein.getText()));
        this.getSystemFacede().getBesinSystem().getBesin().setYag(Double.parseDouble(txt_yag.getText()));
        this.getSystemFacede().getBesinSystem().getBesin().setBesinkategori(this.getSystemFacede().getBesinkategoriSystem().getBkdao().find(comboBox_kategori.getSelectedItem().toString()));
        
        this.getSystemFacede().getBesinSystem().update();
        
        this.yemekGoster("");
        this.alanlarıSıfırla();

    }//GEN-LAST:event_btn_guncelleActionPerformed

