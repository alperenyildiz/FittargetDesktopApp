    public void admin_goster(Admin admin) {
        this.setModel((DefaultTableModel) table_admin.getModel());
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        Object[] row = new Object[11];
        row[0] = admin.getAd_soyad();
        row[1] = admin.getYas();
        row[2] = admin.getCinsiyet();
        row[3] = admin.getEmail();
        row[4] = admin.getKayit_tarihi();
        model.addRow(row);
    }
    
    public void ComboBoxkategoriGoster() {
        String[] kategoriler = new String[this.getSystemFacede().getBesinkategoriSystem().getBklist().size()];
        for (int i = 0; i < kategoriler.length; i++) {
            kategoriler[i] = this.getSystemFacede().getBesinkategoriSystem().getBklist().get(i).getBesinkategori_isim();
        }
        ComboBoxModel modelcombo = new DefaultComboBoxModel(kategoriler);
        comboBox_kategori.setModel(modelcombo);
    }
    
    public void yemekGoster(String deger) {
        List<Besin> bl = this.getSystemFacede().getBesinSystem().getBdao().ara(deger);
        this.model = (DefaultTableModel) table_besin.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        Object[] row = new Object[7];
        for (int i = 0; i < bl.size(); i++) {
            row[0] = bl.get(i).getBesin_id();
            row[1] = bl.get(i).getIsim();
            row[2] = bl.get(i).getKalori();
            row[3] = bl.get(i).getKarbonhidrat();
            row[4] = bl.get(i).getProtein();
            row[5] = bl.get(i).getYag();
            row[6] = bl.get(i).getBesinkategori().getBesinkategori_isim();
            model.addRow(row);
        }
    }
    
    public void kategoriGoster() {
        List<Besinkategori> bklist = this.getSystemFacede().getBesinkategoriSystem().getBklist();
        this.model = (DefaultTableModel) table_kategori.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        Object[] row = new Object[2];
        for (int i = 0; i < bklist.size(); i++) {
            row[0] = bklist.get(i).getBesinkategori_id();
            row[1] = bklist.get(i).getBesinkategori_isim();
            model.addRow(row);
        }
    }
    
    public void alanlarıSıfırla() {
        txt_isim.setText("");
        txt_karbonhidrat.setText("");
        txt_protein.setText("");
        txt_yag.setText("");
        txt_kalori.setText("");
        comboBox_kategori.setSelectedItem(null);
        txt_kategori_isim.setText("");
        
    }
    
